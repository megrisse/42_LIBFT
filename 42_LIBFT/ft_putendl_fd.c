#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	if (!s)
	{
		ft_putstr_fd(0, fd);
	}
	else
	{
		ft_putstr_fd(s, fd);
		write(fd, "\n", 1);
	}
}
